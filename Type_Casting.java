import java.util.*;
public class Type_Casting {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        float a = 25.12f;
        int b = (int) a;
        System.out.println(b);
        char ch = 'A';
        int num = (int) ch;
        System.out.println(num);
        char ch2 = 'b';
        int num2 = (int) ch2;
        System.out.println(num2);
    }
    
}
